/// «Интересное об алгоритме»: нестандартные особенности нашей модели прогноза,
/// чтобы показать пользователю, насколько глубоко мы заморочились.
///
/// Это вечнозелёный пояснительный контент (не зависит от погоды), но в отличие
/// от практических «Советов» он рассказывает про САМ алгоритм. Порядок enum —
/// каноничный; показываем по одному факту, авто-ротация по календарному дню
/// (см. [algoFactOfDayIndex]). Тексты/иконки берутся в presentation.
enum AlgoFact {
  waterModel,
  thermalInertia,
  pressureTrend,
  frontMemory,
  weakestLink,
  heatCalm,
  realSun,
  spawnPhysics,
  speciesModels,
  biteWindows,
}

/// Все факты в каноничном порядке.
const List<AlgoFact> kAlgoFacts = AlgoFact.values;

/// Индекс «факта дня»: детерминирован по календарному дню, чтобы при каждом
/// заходе в один день показывался один и тот же факт, а назавтра — другой.
///
/// Шаг 7 взаимно прост с длиной (10), поэтому индексы идут перемешанной
/// последовательностью и обходят все факты без повторов за полный цикл —
/// ощущается случайным, оставаясь стабильным в пределах суток.
int algoFactOfDayIndex(DateTime now) {
  final daysSinceEpoch = now.toUtc().difference(DateTime.utc(2020)).inDays;
  return (daysSinceEpoch * 7) % kAlgoFacts.length;
}
